# PROBLEM 8:
# Найдите, какой процент этого столетия мы прожили:
# формула расчета процентов
# (a/b)*100
# где a-прожитые года
# b-сколько нужно(тк. это столетие, b-100)

my_age = 18
print("Мне", my_age, "лет")

years = 83
print("Я хочу прожить", years, "года")

res = (my_age / years) * 100
print(res, "процентов я прожил, оказывается я потратил", res, "процентов своей жизни")